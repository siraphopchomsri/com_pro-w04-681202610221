  // ในขณะที่ใน `coding_w04-05-1.c` จะพิมพ์ค่า `Counter = 1` ในครั้งแรก และ `Counter = 2` ในครั้งที่สอง
  // นี่แสดงให้เห็นถึงความแตกต่างระหว่างตัวแปร static และ non-static ในการเก็บค่าระหว่างการเรียกใช้ฟังก์ชัน
//สรปผลการทดลอง
